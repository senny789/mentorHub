const movementReducer = (state: any, action: any) => {
	let speed = 10;
	switch (action.type) {
		case "RIGHT":
			return { ...state, left: state.left + speed };
		case "LEFT":
			return { ...state, left: state.left - speed };
		case "UP":
			return { ...state, up: state.up - speed };
		case "DOWN":
			return { ...state, up: state.up + speed };
		default:
			return state;
	}
};
